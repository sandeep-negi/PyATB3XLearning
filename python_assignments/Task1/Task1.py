# Difference between = operator and == operator in Python

"""
The = operator in Python is used for assignment for example a = 10,
while the == operator is used for comparison for example a == b

"""

# What does ** operator do in Python, and how it is used ?
"""
The  ** operator in Python is called the "exponentiation" operator.
This operation raises the base to the power of the 
exponent . For example:
print(2 ** 3)  # Output: 8 (2 raised to the power of 3)
print(5 ** 2)  # Output: 25 (5 squared)

"""

# What does ^ operator do in Python and in what context it is used ?
"""
In Python, the ^ operator is the bitwise XOR (exclusive OR) operator. 
It performs the XOR operation on the binary representations of the operands.

a = 5    # Binary: 0101
b = 3    # Binary: 0011
c = a ^ b  # Binary: 0110 (Decimal: 6)
print(c)  # Output: 6
"""